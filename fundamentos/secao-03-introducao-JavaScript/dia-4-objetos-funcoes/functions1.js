function soma(numA,numB) {
    return numA + numB
}

function subtracao(numA,numB) {
    return numA - numB
}

function multiplicacao(numA,numB) {
    return numA * numB
}

function divisao(numA,numB) {
    return numA / numB
}

function modulo(numA,numB) {
    return numA % numB
}

console.log(soma(20,20));
console.log(subtracao(20,20));
console.log(multiplicacao(20,20));
console.log(divisao(20,20));
console.log(modulo(20,20));
